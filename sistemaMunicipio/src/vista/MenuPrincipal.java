    private void cargarDatosPo(){
    datosPobladores = new ArrayList<Object[]>();
    pobladores = (List<Poblador>)pcon.findPobladorEntities();
    
    for(Poblador pobla: pobladores){
        Object []  campos = deCamposArregloPo(pobla);
        datosPobladores.add(campos);

    }
    mPo = new ModeloPobladores(datosPobladores);
    }
    
    private Object [] deCamposArregloPo(Poblador po){
                Object [] campos = new Object[5];
                int c = 0;
                campos[c] = po.getIdpoblador();
                campos[c+1] = po.getNombre();
                campos[c+2] = po.getApellidop();
                campos[c+3] = po.getApellidom();
                campos[c+4] = po.getFechanacimiento();
                
                return campos;
    
    }

